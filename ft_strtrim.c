#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	int		i;
	int		len;
	char	*s;

	i = 0;
	if (!s1 || !set)
		return (0);
	while (s1[i] && ft_strchr(set, *s1))
		s1++;
	len = ft_strlen(s1);
	while (len && ft_strchr(set, s1[len - 1]))
		len--;
	s = (char *)malloc(sizeof(*s1) * (len + 1));
	if (!s)
		return (0);
	while (len--)
	{
		s[i] = s1[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
